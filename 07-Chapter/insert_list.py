# This program demonstrates the insert method

def main():
    # create a list
    names = ['James', 'George', 'Sally']

    # display the list
    print('Original list:')
    print(names)

    # insert a new name at element 0
    names.insert(0, 'Sidney')

    # display the list again
    print('New list:')
    print(names)

main()
